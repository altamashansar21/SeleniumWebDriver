package day11;

public class Greetings {
	
	// 1) No Parameters  --> No returns value
	void m1(){
		System.out.println("Altamash");
		
		}

	// 2) No Parameters --> Returns Value
	
	String m2 () {
		return("Hii Altamash How are you?");
	}
	
	// 3) Takes Parameters  --> No return values 
	void m3(String name){
		System.out.println("Hello "+name);
	}
		
		// 4) Take Parameters  --> Return value
		
		String m4(String tag){
			return("Good Morning "+tag);
		}
	}


